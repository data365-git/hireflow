export function tr(lang: Lang, key: string, vars: Record<string, string | number> = {}): string {
  let text = t[lang][key];
  if (!text) {
    console.warn(`[i18n] MISSING key=${key} lang=${lang}`);
    text = t["en"][key] ?? `[!${key}!]`;
  }
  for (const [k, v] of Object.entries(vars)) {
    text = text.replaceAll(`{${k}}`, String(v));
  }
  return text;
}
